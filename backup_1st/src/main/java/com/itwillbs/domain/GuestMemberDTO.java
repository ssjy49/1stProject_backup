package com.itwillbs.domain;

public class GuestMemberDTO {
	
	private int guestNum; // 게스트 회원 번호
	private String guestId; // 게스트 아이디
	private String guestPassword; // 게스트 패스워드
	private String guestName; // 게스트 이름
	private String guestNickname; // 게스트 닉네임
	private String guestGender; // 게스트 성별
	private String guestBirthday; // 게스트 생년월일
	private String guestPhoneNum; // 게스트 휴대 전화번호
	private String guestEmail; // 게스트 이메일
	private String guestLocation; // 게스트 선호 지역
	private String guestProfileImagePath; // 게스트 프로필 사진 경로
	
	public int getGuestNum() {
		return guestNum;
	}
	public void setGuestNum(int guestNum) {
		this.guestNum = guestNum;
	}
	public String getGuestId() {
		return guestId;
	}
	public void setGuestId(String guestId) {
		this.guestId = guestId;
	}
	public String getGuestPassword() {
		return guestPassword;
	}
	public void setGuestPassword(String guestPassword) {
		this.guestPassword = guestPassword;
	}
	public String getGuestName() {
		return guestName;
	}
	public void setGuestName(String guestName) {
		this.guestName = guestName;
	}
	public String getGuestNickname() {
		return guestNickname;
	}
	public void setGuestNickname(String guestNickname) {
		this.guestNickname = guestNickname;
	}
	public String getGuestGender() {
		return guestGender;
	}
	public void setGuestGender(String guestGender) {
		this.guestGender = guestGender;
	}
	public String getGuestBirthday() {
		return guestBirthday;
	}
	public void setGuestBirthday(String guestBirthday) {
		this.guestBirthday = guestBirthday;
	}
	public String getGuestPhoneNum() {
		return guestPhoneNum;
	}
	public void setGuestPhoneNum(String guestPhoneNum) {
		this.guestPhoneNum = guestPhoneNum;
	}
	public String getGuestEmail() {
		return guestEmail;
	}
	public void setGuestEmail(String guestEmail) {
		this.guestEmail = guestEmail;
	}
	public String getGuestLocation() {
		return guestLocation;
	}
	public void setGuestLocation(String guestLocation) {
		this.guestLocation = guestLocation;
	}
	public String getGuestProfileImagePath() {
		return guestProfileImagePath;
	}
	public void setGuestProfileImagePath(String guestProfileImagePath) {
		this.guestProfileImagePath = guestProfileImagePath;
	}
	
}
