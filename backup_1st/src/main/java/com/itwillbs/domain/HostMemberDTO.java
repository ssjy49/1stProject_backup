package com.itwillbs.domain;

public class HostMemberDTO {

	private int hostNum; // 호스틓 회원번호
	private String hostId; // 호스트 아이디
	private String hostPassword; // 호스트 패스워드
	private String hostName; // 호스트 이름
	private String hostNickname; // 호스트 닉네임
	private String hostGender; // 호스트 성별
	private String hostBirthday; // 호스트 생년월일
	private String hostPhoneNum; // 호스트 휴대 전화번호
	private String hostEmail; // 호스트 이메일
	private String businessNum; // 사업자 번호
	private String mailOrderNum; // 통신판매업 신고번호
	private String hostProfileImagePath; // 호스트 프로필 사진 경로
	
	public int getHostNum() {
		return hostNum;
	}
	public void setHostNum(int hostNum) {
		this.hostNum = hostNum;
	}
	public String getHostId() {
		return hostId;
	}
	public void setHostId(String hostId) {
		this.hostId = hostId;
	}
	public String getHostPassword() {
		return hostPassword;
	}
	public void setHostPassword(String hostPassword) {
		this.hostPassword = hostPassword;
	}
	public String getHostName() {
		return hostName;
	}
	public void setHostName(String hostName) {
		this.hostName = hostName;
	}
	public String getHostNickname() {
		return hostNickname;
	}
	public void setHostNickname(String hostNickname) {
		this.hostNickname = hostNickname;
	}
	public String getHostGender() {
		return hostGender;
	}
	public void setHostGender(String hostGender) {
		this.hostGender = hostGender;
	}
	public String getHostBirthday() {
		return hostBirthday;
	}
	public void setHostBirthday(String hostBirthday) {
		this.hostBirthday = hostBirthday;
	}
	public String getHostPhoneNum() {
		return hostPhoneNum;
	}
	public void setHostPhoneNum(String hostPhoneNum) {
		this.hostPhoneNum = hostPhoneNum;
	}
	public String getHostEmail() {
		return hostEmail;
	}
	public void setHostEmail(String hostEmail) {
		this.hostEmail = hostEmail;
	}
	public String getBusinessNum() {
		return businessNum;
	}
	public void setBusinessNum(String businessNum) {
		this.businessNum = businessNum;
	}
	public String getMailOrderNum() {
		return mailOrderNum;
	}
	public void setMailOrderNum(String mailOrderNum) {
		this.mailOrderNum = mailOrderNum;
	}
	public String getHostProfileImagePath() {
		return hostProfileImagePath;
	}
	public void setHostProfileImagePath(String hostProfileImagePath) {
		this.hostProfileImagePath = hostProfileImagePath;
	}
	
	
}
